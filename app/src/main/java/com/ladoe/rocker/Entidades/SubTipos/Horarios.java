package com.ladoe.rocker.Entidades.SubTipos;

public class Horarios {
    private String desdeDia;
    private String hastaDia;
    private String desdeHora;
    private String hastaHora;

    public String getDesdeDia() {
        return desdeDia;
    }

    public void setDesdeDia(String desdeDia) {
        this.desdeDia = desdeDia;
    }

    public String getHastaDia() {
        return hastaDia;
    }

    public void setHastaDia(String hastaDia) {
        this.hastaDia = hastaDia;
    }

    public String getDesdeHora() {
        return desdeHora;
    }

    public void setDesdeHora(String desdeHora) {
        this.desdeHora = desdeHora;
    }

    public String getHastaHora() {
        return hastaHora;
    }

    public void setHastaHora(String hastaHora) {
        this.hastaHora = hastaHora;
    }
}
